package myjavaproject;

class swap2 {

	public static void main(String[] args) {
		int a=10,b=20;
		System.out.println("before swapping");
		System.out.println(a);
		System.out.println(b);
		int temp=a;
		a=b;
		b=temp;
		
		System.out.println("after swapping");
		System.out.println(a);
		System.out.println(b);

	}

	}


